#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0 && (n != -2147483648))
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	if (n != -2147483648)
		write(fd, &"0123456789"[n % 10], 1);
	else
		write(fd, "-2147483648", 11);
}
